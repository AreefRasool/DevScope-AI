def generate_dashboard_data(username):

    report = generate_report(
        username
    )

    if "error" in report:

        return None

    metrics = {

        "Score":
        report["score"],

        "Grade":
        report["grade"],

        "Repositories":
        report["repositories"],

        "Followers":
        report["followers"],

        "Stars":
        report["stars"],

        "Forks":
        report["forks"]

    }

    return metrics


# ============================================
# COMPLETE ANALYTICS
# ============================================

def generate_analytics(username):

    report = generate_report(
        username
    )

    if "error" in report:

        return None

    language_chart = create_language_chart(
        report
    )

    repo_chart = create_repo_chart(
        username
    )

    metrics = generate_dashboard_data(
        username
    )

    return {

        "report":
        report,

        "metrics":
        metrics,

        "language_chart":
        language_chart,

        "repo_chart":
        repo_chart

    }

