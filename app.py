import requests
from datetime import datetime

GITHUB_API = "https://api.github.com/users"


class GitHubAnalyzer:

    # =========================
    # PROFILE
    # =========================

    def get_profile(self, username):

        response = requests.get(
            f"{GITHUB_API}/{username}"
        )

        if response.status_code != 200:
            return None

        return response.json()

    # =========================
    # REPOSITORIES
    # =========================

    def get_repositories(self, username):

        response = requests.get(
            f"{GITHUB_API}/{username}/repos?per_page=100"
        )

        if response.status_code != 200:
            return []

        return response.json()

    # =========================
    # LANGUAGES
    # =========================

    def analyze_languages(self, repos):

        languages = {}

        for repo in repos:

            lang = repo.get("language")

            if lang:

                languages[lang] = (
                    languages.get(lang, 0) + 1
                )

        return languages

    # =========================
    # STARS
    # =========================

    def total_stars(self, repos):

        return sum(
            repo.get(
                "stargazers_count",
                0
            )
            for repo in repos
        )

    # =========================
    # FORKS
    # =========================

    def total_forks(self, repos):

        return sum(
            repo.get(
                "forks_count",
                0
            )
            for repo in repos
        )

    # =========================
    # ACCOUNT AGE
    # =========================

    def account_age(self, profile):

        created = profile.get(
            "created_at"
        )

        if not created:
            return 0

        created = datetime.strptime(
            created,
            "%Y-%m-%dT%H:%M:%SZ"
        )

        return (
            datetime.now() - created
        ).days // 365

    # =========================
    # MOST STARRED REPO
    # =========================

    def most_starred_repo(self, repos):

        if not repos:
            return None

        return max(
            repos,
            key=lambda x:
            x.get(
                "stargazers_count",
                0
            )
        )

    # =========================
    # REPOSITORY SCORE
    # =========================

    def repository_score(
        self,
        repo_count
    ):

        if repo_count >= 20:
            return 20

        elif repo_count >= 10:
            return 15

        elif repo_count >= 5:
            return 10

        return 5

    # =========================
    # STAR SCORE
    # =========================

    def star_score(
        self,
        stars
    ):

        if stars >= 100:
            return 10

        elif stars >= 25:
            return 7

        elif stars >= 5:
            return 4

        return 1

    # =========================
    # FOLLOWER SCORE
    # =========================

    def follower_score(
        self,
        followers
    ):

        if followers >= 500:
            return 10

        elif followers >= 100:
            return 8

        elif followers >= 25:
            return 6

        elif followers >= 5:
            return 3

        return 1

    # =========================
    # LANGUAGE SCORE
    # =========================

    def language_score(
        self,
        languages
    ):

        count = len(languages)

        if count >= 5:
            return 10

        elif count >= 3:
            return 7

        return 4

    # =========================
    # README SCORE
    # =========================

    def readme_presence_score(
        self,
        repos
    ):

        if not repos:
            return 0

        score = 0

        checked = min(
            len(repos),
            20
        )

        for repo in repos[:20]:

            if repo.get(
                "description"
            ):
                score += 1

        percentage = (
            score / checked
        ) * 100

        if percentage >= 80:
            return 15

        elif percentage >= 50:
            return 10

        return 5

    # =========================
    # DOCUMENTATION QUALITY
    # =========================

    def documentation_quality_score(
        self,
        repos
    ):

        if not repos:
            return 0

        score = 0

        checked = min(
            len(repos),
            20
        )

        for repo in repos[:20]:

            repo_score = 0

            if repo.get(
                "description"
            ):
                repo_score += 1

            if repo.get(
                "homepage"
            ):
                repo_score += 1

            score += repo_score

        percentage = (
            score /
            (checked * 2)
        ) * 100

        if percentage >= 80:
            return 15

        elif percentage >= 50:
            return 10

        return 5

    # =========================
    # DIVERSITY SCORE
    # =========================

    def diversity_score(
        self,
        repos
    ):

        categories = {
            "ai": 0,
            "web": 0,
            "data": 0,
            "api": 0,
            "automation": 0
        }

        for repo in repos:

            name = repo.get(
                "name",
                ""
            ).lower()

            if "ai" in name:
                categories["ai"] += 1

            if "web" in name:
                categories["web"] += 1

            if "data" in name:
                categories["data"] += 1

            if "api" in name:
                categories["api"] += 1

            if "bot" in name:
                categories["automation"] += 1

        count = sum(
            1
            for v in categories.values()
            if v > 0
        )

        if count >= 3:
            return 10

        return 4

    # =========================
    # ACTIVITY SCORE
    # =========================

    def activity_score(
        self,
        repos
    ):

        if not repos:
            return 0

        latest = max(
            repos,
            key=lambda x:
            x.get(
                "updated_at",
                ""
            )
        )

        updated = datetime.strptime(
            latest["updated_at"],
            "%Y-%m-%dT%H:%M:%SZ"
        )

        days = (
            datetime.now() - updated
        ).days

        if days <= 30:
            return 10

        elif days <= 90:
            return 8

        elif days <= 180:
            return 5

        return 2

    # =========================
    # FINAL SCORE
    # =========================

    def portfolio_score(
        self,
        profile,
        repos
    ):

        languages = self.analyze_languages(
            repos
        )

        score = 0

        score += self.repository_score(
            len(repos)
        )

        score += self.star_score(
            self.total_stars(repos)
        )

        score += self.follower_score(
            profile.get(
                "followers",
                0
            )
        )

        score += self.language_score(
            languages
        )

        score += self.readme_presence_score(
            repos
        )

        score += self.documentation_quality_score(
            repos
        )

        score += self.diversity_score(
            repos
        )

        score += self.activity_score(
            repos
        )

        return min(
            score,
            100
        )


