analyzer = GitHubAnalyzer()


# =========================
# GRADE
# =========================

def calculate_grade(score):

    if score >= 90:
        return "A+"
    elif score >= 80:
        return "A"
    elif score >= 70:
        return "B"
    elif score >= 60:
        return "C"
    elif score >= 50:
        return "D"

    return "F"


# =========================
# REPORT GENERATOR
# =========================

def generate_report(username):

    profile = analyzer.get_profile(
        username
    )

    if profile is None:

        return {
            "error":
            "GitHub user not found"
        }

    repos = analyzer.get_repositories(
        username
    )

    score = analyzer.portfolio_score(
        profile,
        repos
    )

    return {

        "username":
        username,

        "name":
        profile.get(
            "name",
            "N/A"
        ),

        "followers":
        profile.get(
            "followers",
            0
        ),

        "following":
        profile.get(
            "following",
            0
        ),

        "repositories":
        len(repos),

        "stars":
        analyzer.total_stars(
            repos
        ),

        "forks":
        analyzer.total_forks(
            repos
        ),

        "languages":
        analyzer.analyze_languages(
            repos
        ),

        "score":
        score,

        "grade":
        calculate_grade(
            score
        )
    }


