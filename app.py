import pandas as pd
import plotly.express as px


# ============================================
# LANGUAGE PIE CHART
# ============================================

def create_language_chart(report):

    languages = report["languages"]

    if not languages:

        return None

    df = pd.DataFrame({

        "Language": list(languages.keys()),
        "Count": list(languages.values())

    })

    fig = px.pie(
        df,
        names="Language",
        values="Count",
        title="Programming Languages Distribution",
        hole=0.45
    )

    fig.update_layout(
        height=500
    )

    return fig


# ============================================
# TOP REPOSITORIES CHART
# ============================================

def create_repo_chart(username):

    repos = analyzer.get_repositories(
        username
    )

    if not repos:

        return None

    repo_data = []

    for repo in repos:

        repo_data.append({

            "Repository":
            repo["name"],

            "Stars":
            repo["stargazers_count"]

        })

    df = pd.DataFrame(
        repo_data
    )

    df = df.sort_values(
        by="Stars",
        ascending=False
    ).head(10)

    fig = px.bar(

        df,

        x="Stars",

        y="Repository",

        orientation="h",

        title="Top Repositories by Stars"

    )

    fig.update_layout(
        height=500
    )

    return fig


# ============================================
